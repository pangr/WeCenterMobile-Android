package bean;

public class User {

	private String user_name = "";
	private String avatar_file = "";
	private String fans_count = "";
	private String friend_count = "";
	private String question_count = "";
	private String answer_count = "";
	private String agree_count = "";
	private String topic_focus_count = "";
	private String thanks_count = "";
	private String answer_favorite_count = "";

	public String getUser_name() {
		return user_name;
	}

	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}

	public String getAvatar_file() {
		return avatar_file;
	}

	public void setAvatar_file(String avatar_file) {
		this.avatar_file = avatar_file;
	}

	public String getFans_count() {
		return fans_count;
	}

	public void setFans_count(String fans_count) {
		this.fans_count = fans_count;
	}

	public String getFriend_count() {
		return friend_count;
	}

	public void setFriend_count(String friend_count) {
		this.friend_count = friend_count;
	}

	public String getQuestion_count() {
		return question_count;
	}

	public void setQuestion_count(String question_count) {
		this.question_count = question_count;
	}

	public String getAnswer_count() {
		return answer_count;
	}

	public void setAnswer_count(String answer_count) {
		this.answer_count = answer_count;
	}

	public String getAgree_count() {
		return agree_count;
	}

	public void setAgree_count(String agree_count) {
		this.agree_count = agree_count;
	}

	public String getTopic_focus_count() {
		return topic_focus_count;
	}

	public void setTopic_focus_count(String topic_focus_count) {
		this.topic_focus_count = topic_focus_count;
	}

	public String getThanks_count() {
		return thanks_count;
	}

	public void setThanks_count(String thanks_count) {
		this.thanks_count = thanks_count;
	}

	public String getAnswer_favorite_count() {
		return answer_favorite_count;
	}

	public void setAnswer_favorite_count(String answer_favorite_count) {
		this.answer_favorite_count = answer_favorite_count;
	}

}
